package CoreJava;

public class Loops {
    public static void main(String[] args) {
        // for loop
        for(int i=0;i<5;i++) System.out.println("Hi");

        // while loop
        int j=0;
        while(j<5){
            System.out.println("hi");
            j++;
        }

        // do while loop
        int k=0;
        do {
            System.out.println("hiii");
            k++;
        }while (k<5);
    }
}
