package Week_1.CoreJava;

public class Loops {
    public static void main(Strings[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println("Hi");
        }

        int j = 0;
        while (j < 5) {
            System.out.println("hi");
            j++;
        }

        int k = 0;
        do {
            System.out.println("hiii");
            k++;
        } while (k < 5);
    }
}
