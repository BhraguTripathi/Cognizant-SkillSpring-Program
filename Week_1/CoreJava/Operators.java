package Week_1.CoreJava;

public class Operators {
    public static void main(Strings[] args) {
        int a = 10;
        int b = 20;
        System.out.println(a + b);
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a & b);
    }
}
